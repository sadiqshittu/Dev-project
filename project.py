def text_to_ascii(text):
  """Convert text string to ASCII art"""
  ascii_dict = get_ascii_art()
  text = text.upper()
  
  # Initialize 5 lines for the output
  lines = ["", "", "", "", ""]
  
  for char in text:
    if char in ascii_dict:
      char_art = ascii_dict[char]
      for i in range(5):
        lines[i] += char_art[i] + " "
    else:
      # For unsupported characters, add spaces
      for i in range(5):
        lines[i] += "      "
  
  return '\n'.join(lines)

